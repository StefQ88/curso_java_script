
// almaceno en memoria valores y referencias a un objeto, camelcase

// var
// var fue la forma original de declarar variables, NO HAY QUE CREAR VARIABLES COMO VAR

var helloWord = "Hola, JavaScript"
console.log(helloWord)

helloWord = "Hola de nuevo, JavaScript"
console.log(helloWord)

// let
// ESTA ES LA MANERA CORRECTA DE CREAR VARIABLES QUE MODIFICAN VALOR

let helloWord2 = "Hola, JavaScript 2"
console.log(helloWord2)

helloWord2 = "Hola de nuevo, JavaScript 2"
console.log(helloWord2)

// const
/* ESTA ES LA MANERA CORRECTA DE CREAR VARIABLES QUE NO CAMBIAN VALOR, 
NO SE PUEDE RE ASIGNAR UNA PROPIEDAD QUE ES CONSTANTE PERO SI UNA REFERENCIA COMO UN ARRAY*/
const helloWord3 = "Hola, JavaScript 3"
console.log(helloWord3)